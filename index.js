const add = (x, y) => {
  return x + y;
};

const multiply = (x, y) => {
  return x * y;
}

const divide = (x, y) => {
  return x / y;
};

const square = (x) => {
  return x * x;
}

console.log(add(5, 10));
console.log(divide(5, 10));
console.log(multiply(5, 25));
console.log(square(5));
